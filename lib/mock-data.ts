function buildRepresentativeSentimentResponses(seed: RepresentativeSentimentProfileSeed): VoteResponseSummary[] {
  return seed.weeks.flatMap((week) => {
    const overrides = seed.weekOverrides?.[week.weekOf];
    const supportUsers = overrides?.supportUsers ?? seed.supportUsers.slice(0, week.support);
    const mixedUsers = overrides?.mixedUsers ?? seed.mixedUsers.slice(0, week.mixed);
    const opposeUsers = overrides?.opposeUsers ?? seed.opposeUsers.slice(0, week.oppose);
    const questionId = `vote_${seed.profileId.replace("profile_", "")}_week_${week.weekOf.replaceAll("-", "_")}`;

    return [
      ...supportUsers.map((userId, index) => ({
        id: `vote_response_${seed.profileId}_${week.weekOf}_yes_${index + 1}`.replaceAll("-", "_"),
        userId,
        questionId,
        answer: "yes" as const,
        createdAt: `${week.weekOf}T${String(9 + index).padStart(2, "0")}:10:00.000Z`,
      })),
      ...mixedUsers.map((userId, index) => ({
        id: `vote_response_${seed.profileId}_${week.weekOf}_skip_${index + 1}`.replaceAll("-", "_"),
        userId,
        questionId,
        answer: "skip" as const,
        createdAt: `${week.weekOf}T${String(12 + index).padStart(2, "0")}:20:00.000Z`,
      })),
      ...opposeUsers.map((userId, index) => ({
        id: `vote_response_${seed.profileId}_${week.weekOf}_no_${index + 1}`.replaceAll("-", "_"),
        userId,
        questionId,
        answer: "no" as const,
        createdAt: `${week.weekOf}T${String(15 + index).padStart(2, "0")}:30:00.000Z`,
      })),
    ];
  });
}

