function addDays(dateString: string, days: number) {
  const date = new Date(`${dateString}T00:00:00.000Z`);
  date.setUTCDate(date.getUTCDate() + days);
  return date.toISOString().slice(0, 10);
}

function differenceInDays(start: string, end: string) {
  const startTime = Date.parse(`${start}T00:00:00.000Z`);
  const endTime = Date.parse(`${end}T00:00:00.000Z`);
  return Math.round((endTime - startTime) / (1000 * 60 * 60 * 24));
}

function buildWeeklyRepresentativeSeries(anchors: RepresentativeSentimentWeekSeed[]) {
  if (!anchors.length) {
    return [];
  }

  const sortedAnchors = [...anchors].sort((left, right) => Date.parse(`${left.weekOf}T00:00:00.000Z`) - Date.parse(`${right.weekOf}T00:00:00.000Z`));
  const series: RepresentativeSentimentWeekSeed[] = [];

  sortedAnchors.forEach((anchor, anchorIndex) => {
    const nextAnchor = sortedAnchors[anchorIndex + 1];

    if (!nextAnchor) {
      series.push(anchor);
      return;
    }

    const totalDays = differenceInDays(anchor.weekOf, nextAnchor.weekOf);
    const totalSteps = Math.max(Math.round(totalDays / 7), 1);

    for (let step = 0; step < totalSteps; step += 1) {
      const ratio = step / totalSteps;
      const weekOf = addDays(anchor.weekOf, step * 7);

      series.push({
        weekOf,
        support: Math.round(anchor.support + (nextAnchor.support - anchor.support) * ratio),
        mixed: Math.round(anchor.mixed + (nextAnchor.mixed - anchor.mixed) * ratio),
        oppose: Math.round(anchor.oppose + (nextAnchor.oppose - anchor.oppose) * ratio),
      });
    }
  });

  return series;
}

